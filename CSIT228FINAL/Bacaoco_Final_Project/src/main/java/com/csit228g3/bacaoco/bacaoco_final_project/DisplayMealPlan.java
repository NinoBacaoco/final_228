    public void initData() {
        DefaultTableModel tableModel = (DefaultTableModel) tblMeal.getModel();
        tableModel.setRowCount(0);
        try {
            ResultSet rs = dbHelper.getMealInfoRecords();
            while (rs.next()) {
                int Meal_no = rs.getInt("meal_number");
                String Type = rs.getString("type");
                String Title = rs.getString("title");
                String Description = rs.getString("description");
                                
                Object[] row = {Meal_no, Type, Title, Description};
                addRowToTable(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DisplayMealPlan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void addRowToTable(Object[] dataRows) {
        DefaultTableModel model = (DefaultTableModel) tblMeal.getModel();

        model.addRow(dataRows);
    }
