    private void btnGoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGoMouseClicked
        // TODO add your handling code here:
        String filter = txtFilter.getText();
        Object selectedItem = cmbFilter.getSelectedItem();

        try {
            ResultSet rs = null;
            switch (selectedItem.toString()) {
                case "Meal Number":
                    int mealNumber = Integer.parseInt(filter);
                    rs = dbHelper.getMealInfoByMealNumber(mealNumber);
                    break;
                case "Type":
                    rs = dbHelper.getMealInfoByType(filter);
                    break;
                case "Title":
                    rs = dbHelper.getMealInfoByTitle(filter);
                    break;
                case "Description":
                    rs = dbHelper.getMealInfoByDescription(filter);
                    break;
                default:
                    rs = dbHelper.getMealInfoRecords();
                    break;
            }
            rsAddRow(rs);
        } catch (SQLException e) {
            Logger.getLogger(DisplayMealPlan.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnGoMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblMeal.getModel();
        int selectedRowIndex = tblMeal.getSelectedRow();
        if (selectedRowIndex == -1) {
            return;
        }
        int id = Integer.parseInt(model.getValueAt(selectedRowIndex, 0).toString());
        try {
             int response = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to delete this record?");   
                if (response == 0){
                    System.out.println("ok");
                    dbHelper.deleteMealInfoByMealNumber(id);

                    JOptionPane.showMessageDialog(rootPane, "Record deleted!");
                    initData();
                }else
                JOptionPane.showMessageDialog(rootPane, "Record NOT deleted!");
            
        } catch (SQLException e) {
            Logger.getLogger(DisplayMealPlan.class.getName()).log(Level.SEVERE, null, e);
        }
        
    }//GEN-LAST:event_btnDeleteMouseClicked

